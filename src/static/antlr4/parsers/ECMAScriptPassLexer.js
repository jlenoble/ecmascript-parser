/**
 * Return the next token from the character stream and records this last
 * token in case it resides on the default channel. This recorded token
 * is used to determine when the lexer could possibly match a regex
 * literal.
 *
 */
ECMAScriptPassLexer.prototype.nextToken = function() {

    var next = antlr4.Lexer.prototype.nextToken.call(this);

    if (next.channel == antlr4.Token.DEFAULT_CHANNEL)
        this.lastToken = next;

    return next;
};

/**
 * @returns {Boolean} Returns true if the lexer can match a regex literal.
 */
ECMAScriptPassLexer.prototype.isRegexPossible = function() {

    if (this.lastToken == null)
        return true;

    switch (this.lastToken.type) {
        case ECMAScriptPassLexer.Identifier:
            return false;
        case ECMAScriptPassLexer.NullLiteral:
            return false;
        case ECMAScriptPassLexer.BooleanLiteral:
            return false;
        case ECMAScriptPassLexer.This:
            return false;
        case ECMAScriptPassLexer.CloseBracket:
            return false;
        case ECMAScriptPassLexer.CloseParen:
            return false;
        case ECMAScriptPassLexer.OctalIntegerLiteral:
            return false;
        case ECMAScriptPassLexer.DecimalLiteral:
            return false;
        case ECMAScriptPassLexer.HexIntegerLiteral:
            return false;
        case ECMAScriptPassLexer.StringLiteral:
            return false;
        default:
            return true;
    }
};


